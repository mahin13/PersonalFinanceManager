const fs = require('fs');
const path = require('path');

// Simple 1x1 blue pixel PNG
const bluePng = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D,
  0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
  0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53, 0xDE, 0x00, 0x00, 0x00,
  0x0C, 0x49, 0x44, 0x41, 0x54, 0x08, 0xD7, 0x63, 0x38, 0xE8, 0xF0, 0x0F,
  0x00, 0x01, 0x89, 0x00, 0xE5, 0x38, 0xF5, 0x8B, 0x6D, 0x00, 0x00, 0x00,
  0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82
]);

const assetsDir = path.join(__dirname, 'assets');

if (!fs.existsSync(assetsDir)) {
  fs.mkdirSync(assetsDir);
}

// Create all required asset files
const files = ['icon.png', 'splash.png', 'adaptive-icon.png', 'favicon.png', 'notification-icon.png'];

files.forEach(file => {
  const filePath = path.join(assetsDir, file);
  fs.writeFileSync(filePath, bluePng);
  console.log(`Created: ${file}`);
});

console.log('All assets created successfully!');
